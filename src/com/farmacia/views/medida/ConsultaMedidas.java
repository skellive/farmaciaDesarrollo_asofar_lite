    public MedidaProducto devuelveObjeto(int cas, ArrayList<MedidaProducto> lista) {
        MedidaProducto objeto1 = null;
        for (int i = 0; i < lista.size(); i++) {
            if (cas == lista.get(i).getId_medidas()) {
                objeto1 = lista.get(i);
                break;
            }
        }
        return objeto1;
    }

